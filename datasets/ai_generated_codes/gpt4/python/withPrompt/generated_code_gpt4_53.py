def add(x: int, y: int):
    """
    Add two numbers.

    Args:
    x (int): The first number.
    y (int): The second number.

    Returns:
    int: The sum of the two numbers.

    Example:
    >>> add(2, 3)
    5
    >>> add(5, 7)
    12
    """
    return x + y

# Example tests
print(add(2, 3))  # Output: 5
print(add(5, 7))  # Output: 12
